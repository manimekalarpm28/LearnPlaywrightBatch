let age="pramod";//string literal
let price=19.99;//number literal
let isStudent=true;//boolean literal
let name='Alice';//string literal
let pi=3.14;//number literal
let nullValue=null;//null literal
let undefinedValue=undefined;//undefined literal        

console.log(typeof age); // Output: "string"
console.log(typeof price); // Output: "number"
console.log(typeof isStudent); // Output: "boolean"
console.log(typeof name);   // Output: "string"
console.log(typeof pi); // Output: "number"
console.log(typeof nullValue); // Output: "object" (this is a quirk in JavaScript)
console.log(typeof undefinedValue); // Output: "undefined"  