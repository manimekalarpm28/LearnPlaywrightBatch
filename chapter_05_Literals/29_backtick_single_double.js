// One-line simple explanation and short examples

// Explanation:
// Single and double quotes are identical for plain strings; backticks (template literals)
// allow interpolation and multi-line strings.

console.log('single quotes: no interpolation');
console.log("double quotes: no interpolation");
const name = 'Manimekala';
console.log(`backticks: interpolation -> Hello ${name}`);
console.log(`backticks: multi-line\nline 2`);
